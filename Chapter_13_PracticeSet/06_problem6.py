# Use commands: 
# virtuale env1 and virtual env2 to create two separate environments
# Activate the env1 by using the command:  .\env\Scripts\activate.ps1
# Install any packages like pandas: pip install pandas
# move the installed packages into requirements.txt: pip install > requirements.txt
# and deactivate the env1: deactivate
# then activate env2: .\env\Scripts\activate.ps1
# and to directly get all the installed packages from env1 to env2: pip install -r requirements.txt
# this will copy all the existing libraries inside the env1 to env2